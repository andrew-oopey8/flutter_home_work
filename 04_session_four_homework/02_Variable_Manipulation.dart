// Variable Manipulation
// Topic: Variables, Data types (int, double, String, bool)
// Exercise: Create a Dart program that declares and initializes variables of different types (int, double, String, bool).
// Calculate the sum of two numbers, concatenate two strings, and print out the results.

void main() {
  // Declare and initialize variables of different types
  int num1 = 10;
  int num2 = 20;
  double num3 = 3.14;
  String name1 = 'Andrew';
  String name2 = 'Ayoub';
  bool isLearningDart = true;

  // Calculate the sum of two integers
  int sum = num1 + num2;

  // Concatenate two strings
  String name3 = name1 + name2;

  // Print out the results
  print('Sum of $num1 and $num2 is: $sum');
  print('Concatenated Strings: $name3');
  print('Pi value: $num3');
  print('Is learning Dart: $isLearningDart');
}
